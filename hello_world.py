
# print Hello world
print("Hello Python world!")

# variable
message = "Hello Python world!"
print(message)

# change variable value
message = "Hello Python Crash Course world!"
print(message)

# 1. 变量名只能包含字母、数字和下划线。
# 2. 变量名不能包含空格。
# 3. 不要将Python关键字和函数名用作变量名。
# 4. 变量名应简短且具有描述性。
# 5. 慎用小写字母l和大写字母O，可能会被识别为数字1和0。

